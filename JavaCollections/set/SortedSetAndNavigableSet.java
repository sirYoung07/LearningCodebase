package JavaCollections.set;

public class SortedSetAndNavigableSet {
//    SortedSet
//    The SortedSet interface extends the Set interface.
//    The SortedSet provides a complete ordering of the elements.
//        The default ordering is by Natural order else it is ordered by a Comparator specified at the time of construction.
//    The traversing typically is in ascending order of elements.
//
//8. NavigableSet
//    The NavigableSet interface extends from the SortedSet interface.
//    In addition to the methods of the SortedSet, NavigableSet has navigation methods that give closest matches such as floor, ceiling, lower and higher.
//    A NavigableSet can be traversed in ascending and descending order. Although it allows null element implementations it is discouraged as these implementations can give ambiguous results.
}
